<h2 id="what_is_oo_share">What is OmniOpt2-Share?</h2>

<p>OmniOpt2-Share allows you to Share your results with others, online. You can simply submit a job by
<span class="invert_in_dark_mode"><code class="language-bash">./omniopt_share runs/my_experiment/0</code></span>. The program will upload the
job to our server, and allow give you a link to it which is valid for 30 days.</p>

<h2 class="privacy">Notes on Privacy</h2>
<p>You can chose a random name to which OmniOpt2-Share should call you. But remember: the data you upload
is publically available for 30 days.</p>
